async fn seed_node(
    local_db: &cairn_core::internal::storage::LocalDb,
    project_key: &str,
    issue_number: i64,
    exec_seq: i64,
    node_segment: &str,
) -> (String, String) {
    let project_id = common::create_project(local_db, project_key).await;
    let issue_id = format!("issue-{project_key}-{issue_number}");
    let execution_id = format!("exec-{project_key}-{exec_seq}");
    let job_id = format!("job-{project_key}-{node_segment}");
    local_db
        .execute_script(&format!(
            "\
            INSERT INTO issues(id, project_id, number, title, status, created_at, updated_at)\
             VALUES ('{issue_id}', '{project_id}', {issue_number}, 'Memory issue', 'active', 1, 1);\
            INSERT INTO executions(id, recipe_id, issue_id, project_id, status, started_at, seq)\
             VALUES ('{execution_id}', 'recipe', '{issue_id}', '{project_id}', 'running', 1, {exec_seq});\
            INSERT INTO jobs(id, execution_id, issue_id, project_id, recipe_node_id, node_name, uri_segment, status, created_at, updated_at)\
             VALUES ('{job_id}', '{execution_id}', '{issue_id}', '{project_id}', '{node_segment}', '{node_segment}', '{node_segment}', 'running', 1, 1);"
        ))
        .await
        .unwrap();
    (project_id, job_id)
}

async fn create_node_memory(
    local_db: &cairn_core::internal::storage::LocalDb,
    id: &str,
    name: &str,
    content: &str,
    project_id: &str,
    job_id: &str,
    node_seq: i64,
) -> cairn_core::models::Memory {
    db::create_memory(
        local_db,
        id,
        Some(name),
        content,
        Some(project_id),
        "project",
        project_id,
        Some(job_id),
        Some(node_seq),
        Some("cairn://p/CAIRN/1/1/builder/chat/turn/3"),
    )
    .await
    .unwrap()
}

#[tokio::test]
async fn create_and_load_node_memory() {
    let (_temp, local_db) = common::migrated_db().await;
    let (project_id, job_id) = seed_node(&local_db, "MEM", 1, 1, "builder").await;

    let memory = create_node_memory(
        &local_db,
        "memory-1",
        "tests-before-push",
        "Always run tests before pushing",
        &project_id,
        &job_id,
        1,
    )
    .await;

    assert_eq!(memory.id, "memory-1");
    assert_eq!(memory.name.as_deref(), Some("tests-before-push"));
    assert_eq!(memory.content, "Always run tests before pushing");
    assert_eq!(memory.project_id.as_deref(), Some(project_id.as_str()));
    assert_eq!(memory.status, MemoryStatus::Draft);
    assert_eq!(memory.scope, MemoryScope::Project);
    assert_eq!(memory.scope_value, project_id);
    assert_eq!(memory.job_id.as_deref(), Some(job_id.as_str()));
    assert_eq!(memory.node_seq, Some(1));
    assert_eq!(
        memory.provenance_uri.as_deref(),
        Some("cairn://p/CAIRN/1/1/builder/chat/turn/3")
    );

    let loaded = db::load_memory(&local_db, "memory-1").await.unwrap();
    assert_eq!(loaded.content, memory.content);
}

#[tokio::test]
async fn command_create_uses_uuid_and_keeps_name_display_only() {
    let (_temp, local_db) = common::migrated_db().await;
    let (_project_id, job_id) = seed_node(&local_db, "UUID", 1, 1, "builder").await;

    let input = CreateMemory {
        name: Some("Diesel schema is autogenerated".to_string()),
        content: "edit the migration files instead".to_string(),
        project_id: None,
        scope: MemoryScope::Workspace,
        scope_value: "workspace".to_string(),
        job_id: Some(job_id.clone()),
        node_seq: Some(1),
        provenance_uri: None,
    };

    let first = commands::create(&local_db, input.clone()).await.unwrap();
    let second = commands::create(
        &local_db,
        CreateMemory {
            node_seq: Some(2),
            ..input
        },
    )
    .await
    .unwrap();

    assert_ne!(first.id, "diesel-schema-is-autogenerated");
    assert_ne!(second.id, "diesel-schema-is-autogenerated-2");
    assert_ne!(first.id, second.id);
    assert_eq!(
        first.name.as_deref(),
        Some("Diesel schema is autogenerated")
    );
}

