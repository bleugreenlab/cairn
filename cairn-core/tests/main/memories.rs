#[tokio::test]
async fn load_all_memories_scopes_by_project() {
    let (_temp, local_db) = common::migrated_db().await;
    let (project_a, job_a) = seed_node(&local_db, "AAA", 1, 1, "builder").await;
    let (project_b, job_b) = seed_node(&local_db, "BBB", 1, 1, "builder").await;
    let (_workspace_project, workspace_job) = seed_node(&local_db, "WKS", 1, 1, "builder").await;
    local_db
        .execute(
            "INSERT OR IGNORE INTO projects(id, workspace_id, name, key, repo_path, created_at, updated_at, is_workspace)\
             VALUES ('workspace', 'default', 'Workspace', 'WORKSPACE', '/tmp/workspace', 1, 1, 1)",
            (),
        )
        .await
        .unwrap();

    create_node_memory(
        &local_db,
        "ws",
        "workspace",
        "workspace fact",
        "workspace",
        &workspace_job,
        1,
    )
    .await;
    create_node_memory(&local_db, "a", "a", "project A fact", &project_a, &job_a, 1).await;
    create_node_memory(&local_db, "b", "b", "project B fact", &project_b, &job_b, 1).await;

    let scoped = db::load_all_memories(&local_db, Some(&project_a))
        .await
        .unwrap();
    let ids: Vec<&str> = scoped.iter().map(|m| m.id.as_str()).collect();
    assert!(ids.contains(&"ws"));
    assert!(ids.contains(&"a"));
    assert!(!ids.contains(&"b"));

    let workspace = db::load_all_memories(&local_db, Some("workspace"))
        .await
        .unwrap();
    let ids: Vec<&str> = workspace.iter().map(|m| m.id.as_str()).collect();
    assert_eq!(ids, vec!["ws"]);
}

#[tokio::test]
async fn similar_memory_neighbors_use_node_uris() {
    let (_temp, local_db) = common::migrated_db().await;
    let (project_id, job_id) = seed_node(&local_db, "SIM", 1, 1, "builder").await;
    let query = create_node_memory(
        &local_db,
        "query",
        "query",
        "query",
        &project_id,
        &job_id,
        1,
    )
    .await;
    let neighbor = create_node_memory(
        &local_db,
        "neighbor",
        "neighbor",
        "neighbor",
        &project_id,
        &job_id,
        2,
    )
    .await;
    let query_uri = db::build_node_memory_uri_for_memory(&local_db, &query)
        .await
        .unwrap();
    let neighbor_uri = db::build_node_memory_uri_for_memory(&local_db, &neighbor)
        .await
        .unwrap();

    let vector = vec![1.0_f32, 0.0, 0.0];
    let bytes = cairn_core::internal::embeddings::vector::to_bytes(&vector);
    for uri in [&query_uri, &neighbor_uri] {
        local_db
            .execute(
                "INSERT INTO resource_embeddings(uri, embedding, model, dims, created_at, updated_at)\
                 VALUES (?1, ?2, 'test', 3, 1, 1)",
                params![uri.as_str(), bytes.clone()],
            )
            .await
            .unwrap();
    }

    let neighbors = db::similar_memory_neighbors(&local_db, &query, &query_uri, &[], 0.1, 5)
        .await
        .unwrap();
    assert_eq!(neighbors.len(), 1);
    assert_eq!(neighbors[0].memory.id, "neighbor");
    assert_eq!(neighbors[0].uri, "cairn://p/SIM/1/1/builder/memories/2");
}
