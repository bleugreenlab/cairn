#[tokio::test]
async fn node_memory_sequence_confirms_and_resolves_uri() {
    let (_temp, local_db) = common::migrated_db().await;
    let (project_id, job_id) = seed_node(&local_db, "SEQ", 7, 3, "builder").await;

    let first = create_node_memory(
        &local_db,
        "first",
        "Role fact",
        "Builder should use the node memories target",
        &project_id,
        &job_id,
        1,
    )
    .await;
    let second = create_node_memory(
        &local_db,
        "second",
        "Workspace fact",
        "Workspace scoped content",
        &project_id,
        &job_id,
        2,
    )
    .await;

    assert_eq!(
        db::next_node_memory_seq(&local_db, &job_id).await.unwrap(),
        3
    );

    let uri = db::build_node_memory_uri_for_memory(&local_db, &first)
        .await
        .unwrap();
    assert_eq!(uri, "cairn://p/SEQ/7/3/builder/memories/1");
    let resolved = db::resolve_node_memory_id(&local_db, "SEQ", 7, 3, "builder", 1)
        .await
        .unwrap();
    assert_eq!(resolved.as_deref(), Some("first"));

    let drafts = db::load_draft_memories_for_job(&local_db, &job_id)
        .await
        .unwrap();
    assert_eq!(drafts.len(), 2);

    // Since #2141, `confirm_draft_memories_for_job` advances a job's drafts out
    // of `draft` only once the owning issue is merged (drafts stay inert until
    // the work lands). Mark the seeded issue merged so this test exercises the
    // confirm path it asserts.
    local_db
        .execute(
            "UPDATE issues SET merged_at = 1 WHERE id = 'issue-SEQ-7'",
            (),
        )
        .await
        .unwrap();

    let confirmed = db::confirm_draft_memories_for_job(&local_db, &job_id)
        .await
        .unwrap();
    assert_eq!(confirmed.len(), 2);
    assert_eq!(
        db::load_memory(&local_db, &first.id).await.unwrap().status,
        MemoryStatus::Pending
    );
    assert_eq!(
        db::load_memory(&local_db, &second.id).await.unwrap().status,
        MemoryStatus::Pending
    );
}

